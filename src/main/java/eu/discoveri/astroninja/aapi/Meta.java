public class Meta
{
    private double  elong;
    private double  mag;
    private double  radi;
    private double  sz;

    public double getElong() { return elong; }
    public void setElong(double elong) { this.elong = elong; }

    public double getMag() { return mag; }
    public void setMag(double mag) { this.mag = mag; }

    public double getRadi() { return radi; }
    public void setRadi(double radi) { this.radi = radi; }

    public double getSz() { return sz; }
    public void setSz(double sz) { this.sz = sz; }
}
